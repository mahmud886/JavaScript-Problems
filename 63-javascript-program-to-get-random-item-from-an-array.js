// =============================== && Solution && ==================================

const getRandomArray = (arr) => {
    const randomIndex = Math.floor(Math.random() * arr.length);
    // console.log(randomIndex);
    // get random item
    const item = arr[randomIndex];
    return item;
};

const array = [1, 3, 'Random', 5, 6];
const result = getRandomArray(array);
console.log(result);

